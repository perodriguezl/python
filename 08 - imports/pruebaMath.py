import math

print("Raiz Cuadrada de 9: " + str(math.sqrt(9)))
print("Valor Absoluto de -9: " + str(math.fabs(-9)))
print("Aproximacion por Redondeo de 9.8: " + str(math.ceil(9.8)))
print("Aproximacion por truncamiento de 9.8: " + str(math.floor(9.8)))
print("Calculo exponencial de Base 2 Potencia 3: " + str(math.pow(2,3)))
